num = input('Enter a one digit number (0 - 9): ')
num = int(num)

print('The number', num, 'in words is:')

if num == 0:
    print('ZERO')
elif num == 1:
    print('ONE')
elif num == 2:
    print('TWO')
elif num == 3:
    print('THREE')
elif num == 4:
    print('FOUR')
elif num == 5:
    print('FIVE')
elif num == 6:
    print('SIX')
elif num == 7:
    print('SEVEN')
elif num == 8:
    print('EIGHT')
elif num == 9:
    print('NINE')
else:
    print('INVALID')

